# Escape Sequence Character
# (\" \") to keep something into double qutation we use this syntax
print("I am a \"Good Boy\" ")
print("and he also a Good Boy") 

# We can keep multiple value in print statement
print("Hey", 6 , 7)

# To join multiple character we can use (sep="~")
print("Hey", 6 , 7, sep="~", end="356")